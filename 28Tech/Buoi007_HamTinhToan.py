import math

a = 4
print(math.sqrt(a))

print(math.isqrt(39)) #Căn bậc hai chỉ lấy phần nguyên 

print(pow(2, 3))

print(math.ceil(2.7)) #Làm tròn lên. Mặc định nhảy lên một số NGUYÊN cao hơn

print(math.factorial(5)) #Tính giai thừa 

print(math.gcd(100, 450)) #Ước chung lớn nhất

print(math.comb(10, 2)) #Tính tổ hợp chập k của n (C)

print(abs(-6)) #Giá trị tuyệt đối

print(max(1, 3, 4 ,5)) #Tìm giá trị lớn nhất

print(sum(1, 5, 7)) #Tính tổng 

